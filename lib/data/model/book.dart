class Book {
  late int id;
  late String picUrl;
  late String title;
  late String writer;
  late String totalPage; // 책 페이지 수
  late String publicationDate; // 책 출판일
  late String createdAt; // 책 밀리의 서재에 등록일
  late String subTitle;
  late String introduction;
  late String sequence;
  late String writerIntroductoin;
  late String review;
  late int ranking; // 인기순
  late int bookCategoryId; // 책 카테고리 번호
  late int bookDataId;

  Book(
      {required this.id,
      required this.picUrl,
      required this.title,
      required this.writer,
      required this.totalPage,
      required this.publicationDate,
      required this.createdAt,
      required this.subTitle,
      required this.introduction,
      required this.sequence,
      required this.writerIntroductoin,
      required this.review,
      required this.ranking,
      required this.bookCategoryId,
      required this.bookDataId});

  // Map 형태로 변환합니다.
  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'picUrl': picUrl,
      'title': title,
      'writer': writer,
      'totalPage': totalPage,
      'publicationDate': publicationDate,
      'createdAt': createdAt,
      'subTitle': subTitle,
      'introduction': introduction,
      'sequence': sequence,
      'writerIntroductoin': writerIntroductoin,
      'review': review,
      'ranking': ranking,
      'bookCategoryId': bookCategoryId,
    };
  }

  // Map 형태로 받아서 Dart 객체로 변환합니다.
  Book.fromJson(Map<String, dynamic> json)
      : id = json["id"],
        picUrl = json["picUrl"],
        title = json["title"],
        writer = json["writer"],
        totalPage = json["totalPage"],
        publicationDate = json["publicationDate"],
        createdAt = json["createdAt"],
        subTitle = json["subTitle"],
        introduction = json["introduction"],
        sequence = json["sequence"],
        writerIntroductoin = json["writerIntroductoin"],
        review = json["review"],
        ranking = json["ranking"],
        bookCategoryId = json["bookCategoryId"],
        bookDataId = json["bookDataId"];
}

